		flip: (dir) => {
			const w = App.State.width,
				h = App.State.height;
			const img = App.DOM.ctx.getImageData(0, 0, w, h);
			const d = img.data;
			const b = new Uint8ClampedArray(d);

			if (dir === "h") {
				for (let y = 0; y < h; y++) {
					for (let x = 0; x < w / 2; x++) {
						const iL = (y * w + x) * 4,
							iR = (y * w + (w - 1 - x)) * 4;
						[b[iL], b[iR]] = [d[iR], d[iL]];
						[b[iL + 1], b[iR + 1]] = [d[iR + 1], d[iL + 1]];
						[b[iL + 2], b[iR + 2]] = [d[iR + 2], d[iL + 2]];
						[b[iL + 3], b[iR + 3]] = [d[iR + 3], d[iL + 3]];
					}
				}
			} else {
				for (let y = 0; y < h / 2; y++) {
					for (let x = 0; x < w; x++) {
						const iT = (y * w + x) * 4,
							iB = ((h - 1 - y) * w + x) * 4;
						[b[iT], b[iB]] = [d[iB], d[iT]];
						[b[iT + 1], b[iB + 1]] = [d[iB + 1], d[iT + 1]];
						[b[iT + 2], b[iB + 2]] = [d[iB + 2], d[iT + 2]];
						[b[iT + 3], b[iB + 3]] = [d[iB + 3], d[iT + 3]];
					}
				}
			}
			App.DOM.ctx.putImageData(new ImageData(b, w, h), 0, 0);
			App.Preview.update();
		},
		invert: () => {
			const img = App.DOM.ctx.getImageData(
				0,
				0,
				App.State.width,
				App.State.height
			);
			const d = img.data;
			for (let i = 0; i < d.length; i += 4) {
				d[i] = 255 - d[i];
				d[i + 1] = 255 - d[i + 1];
				d[i + 2] = 255 - d[i + 2];
			}
			App.DOM.ctx.putImageData(img, 0, 0);
			App.Preview.update();
		},
