function getDefaultSettings() {
  return JSON.parse(JSON.stringify(DEFAULT_SETTINGS));
}

function loadAppSettings() {
  const saved = localStorage.getItem(APP_SETTINGS_KEY);
  if (!saved) return getDefaultSettings();

  try {
    return Object.assign(getDefaultSettings(), JSON.parse(saved));
  } catch (error) {
    return getDefaultSettings();
  }
}

function saveAppSettings() {
  localStorage.setItem(APP_SETTINGS_KEY, JSON.stringify(appSettings));
}

function loadGameHistory() {
  return JSON.parse(localStorage.getItem(HISTORY_KEY) || "[]");
}

function saveGameHistory(history) {
  localStorage.setItem(HISTORY_KEY, JSON.stringify(history));
}

function addGameHistory(gameName, action) {
  const history = loadGameHistory();
  history.unshift({
    gameName,
    action,
    timestamp: new Date().toISOString(),
  });
  if (history.length > HISTORY_LIMIT) history.length = HISTORY_LIMIT;
  saveGameHistory(history);
  return history;
}

function formatHistoryTimestamp(timestamp) {
  const date = new Date(timestamp);
  if (Number.isNaN(date.getTime())) return "Unknown time";
  return date.toLocaleString(undefined, {
    month: "short",
    day: "numeric",
    hour: "2-digit",
    minute: "2-digit",
  });
}

function renderHistorySection() {
  const history = loadGameHistory();
  if (historySummary) {
    const plays = history.filter((entry) => entry.action === "Played").length;
    const wins = history.filter((entry) => entry.action === "Win").length;
    historySummary.textContent = `Recent history: ${plays} plays, ${wins} wins.`;
  }

  if (!historyList) return;
  historyList.innerHTML = "";

  if (!history.length) {
    const item = document.createElement("li");
    item.className = "history-empty";
    item.textContent = "No recent history yet. Start playing games to populate your history.";
    historyList.appendChild(item);
    return;
  }

  history.slice(0, 10).forEach((entry) => {
    const item = document.createElement("li");
    item.className = "history-item";
    item.innerHTML = `
      <div class="history-item__title">${entry.action}: ${entry.gameName}</div>
      <div class="history-item__meta">${formatHistoryTimestamp(entry.timestamp)}</div>
    `;
    historyList.appendChild(item);
  });
}

function clearGameHistory() {
  localStorage.removeItem(HISTORY_KEY);
  renderHistorySection();
  showSettingsToast("Play history cleared.");
}

function applyTheme(theme) {
  const isLight = theme === "light";
  document.body.classList.toggle("light-theme", isLight);
  document.body.classList.remove("text-size-small", "text-size-medium", "text-size-large");
  document.body.classList.add(`text-size-${appSettings.display.textSize}`);
  if (themeToggleButton) themeToggleButton.textContent = isLight ? "🌙" : "🌞";
}

function applyTextSize(size) {
  document.body.classList.remove("text-size-small", "text-size-medium", "text-size-large");
  document.body.classList.add(`text-size-${size}`);
}

function showSettingsToast(message) {
  let toast = document.getElementById("settings-toast");
  if (!toast) {
    toast = document.createElement("div");
    toast.id = "settings-toast";
    toast.className = "settings-toast";
    document.body.appendChild(toast);
  }
  toast.textContent = message;
  toast.classList.add("visible");
  window.clearTimeout(toast.hideTimeout);
  toast.hideTimeout = window.setTimeout(() => toast.classList.remove("visible"), 2600);
}

