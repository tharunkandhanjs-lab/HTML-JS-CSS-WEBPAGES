function handleAccountSubmit(event) {
  event.preventDefault();
  const email = signEmailInput?.value.trim() || appSettings.account.email;
  const username = signUsernameInput?.value.trim() || appSettings.account.username || "Player";
  const phone = signPhoneInput?.value.trim() || appSettings.account.phone;

  appSettings.account = {
    ...appSettings.account,
    signedIn: true,
    email,
    username,
    phone,
  };
  saveAppSettings();
  renderSettingsUI();
  showSettingsToast("Account updated successfully.");
}

function handleSignOut() {
  appSettings.account.signedIn = false;
  saveAppSettings();
  renderSettingsUI();
  showSettingsToast("Signed out successfully.");
}

